					<h1>School Climate &amp; Culture</h1>
					<p class="intro">How much time do school leaders spend on effective instructional leadership? 50%? 75%? According to a Wallace Foundation study, School leaders spend on average 30% on instruction and 70% on issues such as student behavioral infractions, budgets and other operational needs. Many school leaders find themselves being reactive to various non-instructional issues instead of proactively creating, maintaining and re-evaluating their school climate and values.</p>

					<p class="intro">Go To Ms. Sams's works closely with school leaders to create optimal learning environments where every stakeholder is on one accord for student achievement. Through inclusive, fair and concise school-wide structures grounded in data, we work collaboratively to design evidence-based systems that promote a positive and productive culture of learning for all.</p>

					<h2>Creating a School &amp; Community Value System</h2>
					<p>Creating a school and community value system starts with thinking about the innate values you want every stakeholder in your building to promote and uphold. How are values integrated and measured? What structures are in place to provide support for these values to thrive? This workshop helps school leaders to align their vision for school climate and culture with supporting structures that promote a safe, positive and productive learning environment.</p>

					<h2>The Power of Advisory</h2>
					<p>How well do the adults in your building know the students? How well do students know each other? Advisory is a sacred time of the day where adults and students meet to build positive connections and meet students' inherent social and academic needs while providing the space for discovering new individual talents and abilities. We will work closely to ensure that advisory time is being used efficiently and in a way that promotes your school's values.</p>

					<h2>Parent Engagement</h2>
					<p>Parent engagement is essential for the success of a school community. Even the most disengaged parents can be engaged when the culture of the school is inviting and understanding of parents' needs and desires to get involved. How are parents and members of the community engaged? How are they welcomed? Are they an active part of the decision making process of your school? Do they have a voice? Are your staff members in tuned with the cultures and backgrounds of the families they serve? Do you use parent participation data to drive parent engagement and student learning? This workshop challenges school leaders develop an inclusive and interactive parent engagement model for their school.</p>

					<h2>Home Visits</h2>
					<p>Although students spend over half of learning years in school, educators only see the "tip" of the iceberg. What happens in students' lives at home has transformative effects on their learning. A growing number of schools are creating more meaningful collaborations with parents. These schools embrace a philosophy of partnership that goes well beyond the one-way and often discipline-related communications from teachers to parents. 
					When schools adopt a deepened trust between parents and teachers and regular sharing of their respective knowledge and understanding of students' needs, interests and progress, children are more likely to succeed. Home visits help to bridge the gap between home and school. This workshop will enable school leaders to create a strategic plan around home visits that will allow for a more cohesive school community.</p>

					<h2>Peer-to-Peer Youth Development</h2>
					<p>By providing opportunities for youth to create positive change in their school, students invest in their own learning community. Peer organizations allow for students to share information and perspectives while gaining experiential practice in their role as leaders.  We will work with your school's identified peer leaders to become powerful catalysts for change in their schools and communities.</p> 

					<h2>Student Led Conferences</h2>
					<p>How does your learning community celebrate student learning based on their individual growth? When students are active participants in their assessment process, they have an opportunity to increase their intrinsic motivation, which fosters growth. Student Led Conferences places students at the center of their learning. This workshop helps schools design and implement structures that support student led conferences, from establishing a standards-based grading scale to collecting, reflecting and presenting student work.</p>

					<h2>Cultural Competency Training for Urban Educators</h2>
					<p>As a country, we benefit from the education of ALL children. However, how do we not only make learning relevant, but transformational for the communities in which students come from? Cultural competence provides a set of skills that professionals need in order to improve practice to serve all students and communicate effectively with their families. This workshop empowers educators to build on the cultural and language qualities that young people bring to the classroom rather than viewing those qualities as deficits.</p>
